_SEGMENT_FONT_FAMILIES_REGISTERED: list[str] = []
_SEGMENT_FONTS_LOAD_ATTEMPTED = False


def load_segment_digit_fonts() -> None:
    """
    Register ``fonts/*.ttf`` / ``*.otf`` (call once after ``QApplication`` / ``QGuiApplication`` exists).
    Bundled default: DSEG7 Classic (https://github.com/keshikan/DSEG, SIL OFL 1.1).
    """
    global _SEGMENT_FONTS_LOAD_ATTEMPTED, _SEGMENT_FONT_FAMILIES_REGISTERED
    if _SEGMENT_FONTS_LOAD_ATTEMPTED:
        return
    _SEGMENT_FONTS_LOAD_ATTEMPTED = True
    if not FONTS_DIR.is_dir():
        return
    for path in sorted(FONTS_DIR.glob("*.ttf")) + sorted(FONTS_DIR.glob("*.otf")):
        try:
            fid = QFontDatabase.addApplicationFont(str(path))
        except (OSError, TypeError):
            continue
        if fid < 0:
            continue
        _SEGMENT_FONT_FAMILIES_REGISTERED.extend(QFontDatabase.applicationFontFamilies(fid))


def _seven_segment_font_family() -> str:
    """Prefer bundled/system 7-segment face; last resort fixed-pitch (not segment-shaped)."""
    load_segment_digit_fonts()
    for fam in _SEGMENT_FONT_FAMILIES_REGISTERED:
        if fam and QFontDatabase.hasFamily(fam):
            return fam
    for fam in ("DSEG7 Classic", "DSEG7 Modern", "DS-Digital", "Seven Segment", "LCD2"):
        if QFontDatabase.hasFamily(fam):
            return fam
    return _readout_monospace_fallback_family()


def _readout_monospace_fallback_family() -> str:
    for fam in ("Cascadia Mono", "Consolas", "Courier New", "Lucida Console", "monospace"):
        if QFontDatabase.hasFamily(fam):
            return fam
    return "monospace"


