def _format_resistance_xdm1041_datasheet(ohms: float, mode_id: str) -> str:
    """
    Format numeric resistance like the OWON XDM1041 published range table (Ω / kΩ / MΩ LSD).

    Resolutions (approximate bucket by magnitude when autorange is unknown): 500 Ω → 0.01 Ω;
    5 kΩ → 0.0001 kΩ; 50 kΩ → 0.001 kΩ; 500 kΩ → 0.01 kΩ; 5 MΩ → 0.0001 MΩ; 50 MΩ → 0.001 MΩ.
    Continuity row: 0.1 Ω on the 1000 Ω range.

    Below **1 Ω**, values are shown in **milliohms** (``mΩ``): ohms mode uses the same 10 mΩ step
    as 0.01 Ω; continuity keeps 100 mΩ steps (0.1 Ω).

    From **1000 Ω** upward, values use **kΩ** (numeric + ``k`` + ``Ω``); megohm ranges use **M** + **Ω**.
    """
    if not math.isfinite(ohms):
        return "OPEN"
    sign = "-" if ohms < 0 else ""
    r = abs(ohms)

    if mode_id == "continuity" and r < 1000.0:
        if r < 1.0:
            m_mohm = r * 1000.0
            q_m = round(m_mohm / 100.0) * 100.0
            return f"{sign}{int(q_m)} mΩ"
        q = round(r / 0.1) * 0.1
        return f"{sign}{q:.1f}"

    if mode_id == "ohms" and r < 1.0:
        m_mohm = r * 1000.0
        q_m = round(m_mohm / 10.0) * 10.0
        return f"{sign}{int(q_m)} mΩ"

    if r < 1000.0:
        q = round(r / 0.01) * 0.01
        return f"{sign}{q:.2f}"

    k = r / 1000.0
    if r < 5000.0:
        q = round(k / 0.0001) * 0.0001
        return f"{sign}{q:.4f} kΩ"
    if r < 50_000.0:
        q = round(k / 0.001) * 0.001
        return f"{sign}{q:.3f} kΩ"
    if r < 500_000.0:
        q = round(k / 0.01) * 0.01
        return f"{sign}{q:.2f} kΩ"
    if r < 5e6:
        m = r / 1e6
        q = round(m / 0.0001) * 0.0001
        return f"{sign}{q:.4f} MΩ"
    m = r / 1e6
    q = round(m / 0.001) * 0.001
    return f"{sign}{q:.3f} MΩ"


def _format_voltage_xdm1041_datasheet(volts: float) -> str:
    """
    Format DC or AC voltage like the OWON XDM1041 / XDM1000 published range table (primary unit: V).

    Buckets follow the usual 6 voltage ranges when autorange is unknown (same LSD as the datasheet
    max-counts line): 50 mV → 0.001 mV; 500 mV → 0.01 mV; 5 V → 0.0001 V; 50 V → 0.001 V;
    500 V → 0.01 V; 1000 V → 0.1 V.
    """
    if not math.isfinite(volts):
        return "0"
    sign = "-" if volts < 0 else ""
    v = abs(volts)

    if v < 0.05:  # 50.000 mV
        q = round(v / 1e-6) * 1e-6
        return f"{sign}{q:.6f}"
    if v < 0.5:  # 500.00 mV
        q = round(v / 1e-5) * 1e-5
        return f"{sign}{q:.5f}"
    if v < 5.0:  # 5.0000 V
        q = round(v / 1e-4) * 1e-4
        return f"{sign}{q:.4f}"
    if v < 50.0:  # 50.000 V
        q = round(v / 1e-3) * 1e-3
        return f"{sign}{q:.3f}"
    if v < 500.0:  # 500.00 V
        q = round(v / 1e-2) * 1e-2
        return f"{sign}{q:.2f}"
    if v < 1000.0:  # 1000.0 V
        q = round(v / 0.1) * 0.1
        return f"{sign}{q:.1f}"
    q = round(v / 0.1) * 0.1
    return f"{sign}{q:.1f}"


def _normalize_scpi_numeric_token(s: str) -> str:
    """
    Strip whitespace and trailing unit tokens, then normalize commas for ``float``.

    OWON-style replies are usually scientific ASCII; some firmware may append ``Ω``/``OHM`` or use
    a single comma as the decimal separator (e.g. ``2,005E+03``).
    """
    t = (s or "").strip()
    if not t:
        return ""
    t = re.sub(r"[\u03a9\u2126]", "", t)
    t = re.sub(r"(?i)\s*(ohm|ohms)\s*$", "", t).strip()
    t = re.sub(r"\s+", "", t)
    if not t:
        return ""
    tl = t.lower()
    eloc = tl.find("e")
    if eloc >= 0:
        mant, exp_part = t[:eloc], t[eloc:]
    else:
        mant, exp_part = t, ""
    if "," in mant and "." not in mant and mant.count(",") == 1:
        mant = mant.replace(",", ".")
    else:
        mant = mant.replace(",", "")
    return mant + exp_part


def _parse_float_loose(s: str) -> float | None:
    """Parse meter text like ``1E 9``, ``1.2E-03``, ``+2.005E+03 OHM`` after normalization."""
    compact = _normalize_scpi_numeric_token(s)
    if not compact:
        return None
    compact = compact.replace("D", "E").replace("d", "e")
    try:
        return float(compact.replace("E", "e"))
    except ValueError:
        pass
    # Fallback: first scientific or plain float token (SCPI rarely needs leading ``.`` mantissas).
    m = re.search(r"[+-]?(?:\d+\.\d+|\d+)(?:[eE][+-]?\d+)?", compact)
    if not m:
        return None
    try:
        return float(m.group(0).replace("E", "e"))
    except ValueError:
        return None


# Datasheet-style resistance readout: ``2.0050 K`` — split for mixed fonts (DSEG + sans suffix).
_RE_READOUT_KM_SUFFIX = re.compile(r"^([+-]?\d+(?:\.\d+)?)\s+([KM])$")
# Diode forward drop: match physical meter ``… VDC`` (case-insensitive on input).
_RE_READOUT_VDC_SUFFIX = re.compile(r"^([+-]?\d+(?:\.\d+)?)\s+VDC\s*$", re.IGNORECASE)
_RE_READOUT_MOHM_SUFFIX = re.compile(
    r"^([+-]?\d+(?:\.\d+)?)\s+(mΩ|mohm)\s*$",
    re.IGNORECASE,
)
# Collapse ``- - 1.65`` / ``--1.65`` (double polarity from firmware or legacy ``--`` formatting) → ``-1.65``.
_READOUT_LEADING_MINUS_RUN_RE = re.compile(r"^(\s*-\s*)+(?=[\d.])")


def _normalize_readout_signed_numeric_text(s: str) -> str:
    """Single leading minus before a numeric readout; leaves ``OPEN``, ``---``, ``OVER RANGE`` unchanged."""
    t = (s or "").strip()
    if not t:
        return t
    u = t.upper()
    if u == "OPEN" or u == LCD_TEXT_OVER_RANGE.upper() or t == LCD_IDLE_PLACEHOLDER:
        return t
    return _READOUT_LEADING_MINUS_RUN_RE.sub("-", t, count=1)


def _readout_resistance_km_parts(text: str) -> tuple[str, str] | None:
    m = _RE_READOUT_KM_SUFFIX.fullmatch((text or "").strip())
    if not m:
        return None
    return m.group(1), m.group(2)


def _readout_vdc_suffix_parts(text: str) -> tuple[str, str] | None:
    m = _RE_READOUT_VDC_SUFFIX.fullmatch((text or "").strip())
    if not m:
        return None
    return m.group(1), "VDC"


def _readout_mohm_num(text: str) -> str | None:
    m = _RE_READOUT_MOHM_SUFFIX.fullmatch((text or "").strip())
    if not m:
        return None
    return m.group(1)


