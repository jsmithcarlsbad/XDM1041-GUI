def _resolve_disk_icon_path(stem: str) -> Path | None:
    name = f"{stem}.png"
    key = stem.lower()
    for root in _ICON_SEARCH_ROOTS:
        exact = root / name
        if exact.is_file():
            return exact
        if not root.is_dir():
            continue
        try:
            for p in root.iterdir():
                if p.suffix.lower() == ".png" and p.stem.lower() == key:
                    return p
        except OSError:
            continue
    return None


def _load_icon_png_from_disk(stem: str) -> QImage | None:
    path = _resolve_disk_icon_path(stem)
    if path is None:
        return None
    return load_mode_icon_qimage(path)


