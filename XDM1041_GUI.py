def _measurement_readout_unified_segment_px(lbl: QLabel) -> int:
    """
    One 7-segment font size for **all** measurement modes.

    Sized for the widest mantissa the XDM1041-style formatters can emit (see
    ``MEASUREMENT_READOUT_UNIFIED_GLYPH_SLOTS``), so short readings do not use a larger font
    than long ones.
    """
    h = _measurement_readout_height_px(lbl)
    w = _measurement_readout_width_px(lbl)
    n = MEASUREMENT_READOUT_UNIFIED_GLYPH_SLOTS
    by_h = int(h * 0.90)
    by_w = int(w * 0.78 / n)
    raw = min(by_h, by_w)
    return max(22, min(320, int(raw)))


