    def _connect_meter(self) -> None:
        assert self._serial_svc is not None
        device = self._current_port_device()
        if not device:
            if self._status is not None:
                self._status.showMessage("Select a COM port.", 5000)
            return

        baud = self._current_baud()
        params = {
            "port": device,
            "baudrate": baud,
            "bytesize": _bytesize_from_ini(self._cfg.get(INI_SECTION, "databits", fallback="8")),
            "parity": _parity_from_ini(self._cfg.get(INI_SECTION, "parity", fallback="N")),
            "stopbits": _stopbits_from_ini(self._cfg.get(INI_SECTION, "stopbits", fallback="1")),
            "timeout": 1.0,
        }

        if self._status is not None:
            self._status.showMessage(f"Opening {device}…", 3000)

        self._serial_svc.request_open(params)

    def _disconnect_meter(self) -> None:
        assert self._serial_svc is not None
        self._user_requested_disconnect = True
        self._serial_svc.request_close()
        self._persist_connection_ini()

    def _setup_remote_serial(self) -> None:
        if (
            self._combo_remote_port is None
            or self._combo_remote_baud is None
            or self._btn_remote_open is None
        ):
            return
        self._remote_svc = PicoRemoteSerialService(debug_level=self._debug_level)
        self._remote_svc.open_finished.connect(self._on_remote_open_finished)
        self._remote_svc.closed.connect(self._on_remote_closed)
        self._remote_svc.remote_mode.connect(self._on_pico_remote_mode)
        self._remote_svc.remote_acquire.connect(self._on_pico_remote_acquire)
        self._remote_svc.remote_publish_hz.connect(self._on_pico_remote_publish_hz)
        self._remote_svc.remote_pico_version.connect(self._on_pico_remote_version)
        self._remote_svc.start()
        self._app.runtime.pico_remote_serial = self._remote_svc
        self._fill_remote_baud_combo(preferred_baud=self._ini_remote_baud_int())
        self._refresh_remote_port_combo()
        self._combo_remote_port.currentIndexChanged.connect(self._on_remote_port_changed)
        self._btn_remote_open.clicked.connect(self._on_remote_open_clicked)
        self._set_remote_connected_ui(False)

