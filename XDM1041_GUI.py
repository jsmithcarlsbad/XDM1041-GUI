class MeterSerialService(QThread):
    """
    Dedicated thread: sole owner of serial.Serial. GUI enqueues open/close via queue.
    Emits results and errors to the GUI thread.
    """

    open_finished = Signal(bool, str)
    closed = Signal()
    comm_error = Signal(str)
    measurement_received = Signal(str)
    poll_error = Signal(str)

    def __init__(self, debug_level: int = 1) -> None:
        super().__init__()
        self._cmd_q: queue.Queue[tuple[Any, ...]] = queue.Queue()
        self._ser: serial.Serial | None = None
        self._alive = True
        self._debug_level = max(0, min(2, debug_level))
        self._acq_mode_id: str | None = None
        self._acq_rate_id: str | None = None

    def set_debug_level(self, level: int) -> None:
        self._debug_level = max(0, min(2, level))

    def _dbg_conn(self, msg: str) -> None:
        if self._debug_level >= 1:
            print(f"[XDM1041] {msg}", flush=True)

    def _dbg_serial(self, direction: str, payload: str) -> None:
        if self._debug_level >= 2:
            print(f"[XDM1041 serial] {direction} {payload!r}", flush=True)

    def _serial_log(self) -> LogFn | None:
        return self._dbg_serial if self._debug_level >= 2 else None

    def request_open(self, params: dict[str, Any]) -> None:
        self._cmd_q.put(("open", params))

    def request_close(self) -> None:
        self._cmd_q.put(("close",))

    def request_meas_tick(self, mode_id: str, rate_id: str) -> None:
        self._cmd_q.put(("meas_tick", mode_id, rate_id))

    def request_scpi_line(self, line: str) -> None:
        """Send one SCPI line (e.g. AUTO or RANGE n) on the serial thread."""
        self._cmd_q.put(("scpi_line", line))

    def request_shutdown(self) -> None:
        self._cmd_q.put(("shutdown",))

    def _close_port(self) -> None:
        if self._ser is not None:
            try:
                if self._ser.is_open:
                    self._ser.close()
            except OSError as e:
                self.comm_error.emit(f"Close port: {e}")
            self._ser = None

    def _do_open(self, params: dict[str, Any]) -> None:
        self._close_port()
        self._acq_mode_id = None
        self._acq_rate_id = None
        self._dbg_conn(
            f"Opening serial port={params['port']!r} baudrate={params['baudrate']!r} "
            f"bytesize={params['bytesize']!r} parity={params['parity']!r} stopbits={params['stopbits']!r}"
        )
        try:
            self._ser = serial.Serial(
                port=params["port"],
                baudrate=int(params["baudrate"]),
                bytesize=int(params["bytesize"]),
                parity=params["parity"],
                stopbits=float(params["stopbits"]),
                timeout=float(params.get("timeout", 1.0)),
            )
        except (serial.SerialException, ValueError, TypeError) as e:
            self._ser = None
            self._dbg_conn(f"Open failed: {e}")
            self.open_finished.emit(False, str(e))
            return

        log = self._serial_log()
        try:
            idn = _scpi_query(self._ser, "*IDN?", log=log)
        except (serial.SerialException, UnicodeError) as e:
            self._close_port()
            self._dbg_conn(f"*IDN? failed: {e}")
            self.open_finished.emit(False, f"Meter did not respond: {e}")
            return

        self._dbg_conn(f"Connected, *IDN? = {idn!r}")
        self.open_finished.emit(True, idn)

    def _do_close(self) -> None:
        self._close_port()
        self._acq_mode_id = None
        self._acq_rate_id = None
        self._dbg_conn("Serial port closed")
        self.closed.emit()

    def _do_scpi_line(self, line: str) -> None:
        if self._ser is None or not self._ser.is_open:
            return
        log = self._serial_log()
        try:
            _scpi_write_line(self._ser, line, log=log)
            _drain_scpi_lines(self._ser, log=log)
        except (serial.SerialException, UnicodeError) as e:
            self.poll_error.emit(str(e))

    def _do_meas_tick(self, mode_id: str, rate_id: str) -> None:
        if self._ser is None or not self._ser.is_open:
            return
        mode = XDM_MODE_BY_ID.get(mode_id)
        rate = XDM_SAMPLE_RATE_BY_ID.get(rate_id)
        if mode is None or rate is None:
            return
        log = self._serial_log()
        try:
            if self._acq_mode_id != mode_id:
                _scpi_write_line(self._ser, mode.configure_cmd, log=log)
                _drain_scpi_lines(self._ser, log=log)
                self._acq_mode_id = mode_id
            if self._acq_rate_id != rate_id:
                _scpi_write_line(self._ser, rate.rate_cmd, log=log)
                _drain_scpi_lines(self._ser, log=log)
                self._acq_rate_id = rate_id
            val = _scpi_query(self._ser, SCPI_READ_PRIMARY, log=log)
            self.measurement_received.emit(val)
        except (serial.SerialException, UnicodeError) as e:
            self._acq_mode_id = None
            self._acq_rate_id = None
            self.poll_error.emit(str(e))

    def run(self) -> None:
        try:
            while self._alive:
                try:
                    msg = self._cmd_q.get(timeout=0.25)
                except queue.Empty:
                    continue
                kind = msg[0]
                try:
                    if kind == "open":
                        self._do_open(msg[1])
                    elif kind == "close":
                        self._do_close()
                    elif kind == "meas_tick":
                        self._do_meas_tick(msg[1], msg[2])
                    elif kind == "scpi_line":
                        self._do_scpi_line(msg[1])
                    elif kind == "shutdown":
                        self._alive = False
                        self._do_close()
                        break
                except Exception as e:
                    self._close_port()
                    if kind == "open":
                        # GUI shows one dialog from open_finished(False)
                        self.open_finished.emit(False, str(e))
                    else:
                        self.comm_error.emit(f"Serial thread error: {e}")
        finally:
            self._close_port()

    def shutdown_join(self, timeout_ms: int = 5000) -> None:
        self.request_shutdown()
        if not self.wait(timeout_ms):
            self.comm_error.emit("Serial thread did not stop cleanly; terminating.")
            self.terminate()
            self.wait(1000)


class PicoRemoteSerialService(QThread):
    """
    Second serial port for Pico / remote control (no SCPI to the meter on this link).

    Parses newline-terminated ASCII commands and emits Qt signals so the GUI thread can
    mirror mode / acquire state for the operator. Full protocol: ``REMOTE_PROTOCOL.md``.
    """

    open_finished = Signal(bool, str)
    closed = Signal()
    remote_mode = Signal(str)
    remote_acquire = Signal(bool)
    remote_publish_hz = Signal(float)
    remote_pico_version = Signal(str)

    def __init__(self, debug_level: int = 1) -> None:
        super().__init__()
        self._cmd_q: queue.Queue[tuple[Any, ...]] = queue.Queue()
        self._ser: serial.Serial | None = None
        self._alive = True
        self._debug_level = max(0, min(2, debug_level))
        self._rx_buf = bytearray()

    def _dbg(self, msg: str) -> None:
        if self._debug_level >= 1:
            print(f"[remote] {msg}", flush=True)

    def request_open(self, params: dict[str, Any]) -> None:
        self._cmd_q.put(("open", params))

    def request_close(self) -> None:
        self._cmd_q.put(("close",))

    def request_shutdown(self) -> None:
        self._cmd_q.put(("shutdown",))

    def _close_port(self) -> None:
        if self._ser is not None:
            try:
                if self._ser.is_open:
                    self._ser.close()
            except OSError as e:
                self._dbg(f"close: {e}")
            self._ser = None

    def _do_open(self, params: dict[str, Any]) -> None:
        self._close_port()
        self._dbg(
            f"Opening remote port={params['port']!r} baudrate={params['baudrate']!r}"
        )
        try:
            self._ser = serial.Serial(
                port=params["port"],
                baudrate=int(params["baudrate"]),
                bytesize=int(params["bytesize"]),
                parity=params["parity"],
                stopbits=float(params["stopbits"]),
                timeout=0,
                write_timeout=1.0,
            )
        except (serial.SerialException, ValueError, TypeError) as e:
            self._ser = None
            self._dbg(f"Open failed: {e}")
            self.open_finished.emit(False, str(e))
            return
        self.open_finished.emit(True, "Remote port open")
        self._remote_write(b"READY xdm_gui\n")

    def _do_close(self) -> None:
        self._close_port()
        self._rx_buf.clear()
        self._dbg("Remote port closed")
        self.closed.emit()

    def _remote_write(self, data: bytes) -> None:
        if self._ser is None or not self._ser.is_open:
            return
        try:
            self._ser.write(data)
        except serial.SerialException as e:
            self._dbg(f"remote write: {e}")

    def _handle_remote_line(self, line: str) -> None:
        s = line.strip()
        if not s or s.startswith("#"):
            return
        parts = s.split()
        head = parts[0].upper()

        if head == "PING":
            self._remote_write(b"PONG\n")
            return

        tok = s.split(None, 1)
        if len(tok) >= 1 and tok[0].upper() == "PICO":
            ver = tok[1].strip() if len(tok) >= 2 else ""
            if not ver:
                self._remote_write(b"ERR pico_version_missing\n")
                return
            self.remote_pico_version.emit(ver)
            return

        if head.startswith("MODE"):
            if "=" in s:
                rest = s.split("=", 1)[1].strip()
            else:
                rest = s[4:].strip().lstrip(":=").strip()
            if not rest:
                self._remote_write(b"ERR mode_missing\n")
                return
            mid = rest.split()[0].strip().lower()
            if mid not in XDM_MODE_BY_ID:
                self._remote_write(b"ERR unknown_mode\n")
                return
            self.remote_mode.emit(mid)
            return

        if head == "START":
            self.remote_acquire.emit(True)
            return
        if head == "STOP":
            self.remote_acquire.emit(False)
            return
        if head == "ACQ" and len(parts) >= 2:
            v = parts[1].upper()
            if v in ("1", "ON", "TRUE", "YES"):
                self.remote_acquire.emit(True)
            elif v in ("0", "OFF", "FALSE", "NO"):
                self.remote_acquire.emit(False)
            else:
                self._remote_write(b"ERR bad_acq\n")
            return

        if head == "RATE" and len(parts) >= 2:
            try:
                hz = float(parts[1])
                if hz < 0.0:
                    hz = 0.0
                hz = min(hz, 100.0)
                self.remote_publish_hz.emit(hz)
            except ValueError:
                self._remote_write(b"ERR bad_rate\n")
            return

        self._remote_write(b"ERR unknown_cmd\n")

    def _poll_serial_lines(self) -> None:
        if self._ser is None or not self._ser.is_open:
            return
        try:
            n = self._ser.in_waiting
            if n:
                self._rx_buf.extend(self._ser.read(n))
        except serial.SerialException as e:
            self._dbg(f"remote read: {e}")
            self._do_close()
            return

        while True:
            i = self._rx_buf.find(b"\n")
            if i < 0:
                break
            raw = bytes(self._rx_buf[:i])
            del self._rx_buf[: i + 1]
            line = raw.decode("utf-8", errors="replace")
            if line.endswith("\r"):
                line = line[:-1]
            self._handle_remote_line(line)

    def run(self) -> None:
        try:
            while self._alive:
                stop_all = False
                try:
                    msg = self._cmd_q.get(timeout=0.05)
                except queue.Empty:
                    msg = None
                while msg is not None:
                    kind = msg[0]
                    if kind == "open":
                        self._do_open(msg[1])
                    elif kind == "close":
                        self._do_close()
                    elif kind == "shutdown":
                        self._alive = False
                        self._do_close()
                        stop_all = True
                        break
                    try:
                        msg = self._cmd_q.get_nowait()
                    except queue.Empty:
                        msg = None
                if stop_all or not self._alive:
                    break

                self._poll_serial_lines()
        finally:
            self._close_port()

    def shutdown_join(self, timeout_ms: int = 5000) -> None:
        self.request_shutdown()
        if not self.wait(timeout_ms):
            self.terminate()
            self.wait(1000)


class RuntimeResources:
    """Tracks background threads; clean shutdown for meter and optional Pico remote serial."""

    def __init__(self) -> None:
        self._shutdown_done = False
        self.bg_threads: list[threading.Thread | QThread] = []
        self.meter_serial: MeterSerialService | None = None
        self.pico_remote_serial: PicoRemoteSerialService | None = None

    def shutdown(self) -> None:
        if self._shutdown_done:
            return
        self._shutdown_done = True

        if self.pico_remote_serial is not None:
            self.pico_remote_serial.shutdown_join()
            self.pico_remote_serial = None

        if self.meter_serial is not None:
            self.meter_serial.shutdown_join()
            self.meter_serial = None

        for t in self.bg_threads:
            try:
                if isinstance(t, QThread):
                    t.quit()
                    t.wait(3000)
                elif isinstance(t, threading.Thread) and t.is_alive():
                    t.join(timeout=3.0)
            except RuntimeError:
                pass
        self.bg_threads.clear()


